/* Trabalhando com números de bases diferentes */

#include <iostream>

using namespace std;

int main (int args, char ** argv) {

    cout << 25 << endl; /* base decimal */
    cout << 0x19 << endl; /* base hexadecimal */
    cout << 031 << endl; /* base octal */
    cout << 0b11001 << endl; /* base binária */

    return 0;
}