/* Constantes e variáveis */

#include <iostream>

using namespace std;

int main (int argc, char ** argv) {

    char umCaracter = 'I'; 

    cout << "A letra \'I\' tem uma perna." << endl; /* Constante */
    cout << "A letra \'" << umCaracter << "\' tem uma parna." << endl; /* Variável */

    return 0;
}